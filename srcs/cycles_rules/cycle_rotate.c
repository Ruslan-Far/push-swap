#include "../../incs/push_swap.h"
#include "../../libft/libft.h"

void	cra(t_list **a, int n)
{
	int	i;

	i = 0;
	while (i++ < n)
		ra(a);
}

void	crb(t_list **b, int n)
{
	int	i;

	i = 0;
	while (i++ < n)
		rb(b);
}

void	crr(t_list **a, t_list **b, int n)
{
	int	i;

	i = 0;
	while (i++ < n)
		rr(a, b);
}
